public class DoWhile {
	public static void main(String[] args) {
		/**
		 * do while
		 * 格式:
		 * int i =0;
		 * do{
		 * 	
		 * }while()
		 */
		
		int i = 0;
		do {
			i++;
			System.out.println(i);
			
		} while (i<10);
		System.out.println("=============");
		
		int j =0;
		while(j<10){
			j++;
			System.out.println(j);
			
			
		}
	}
	
	
}
